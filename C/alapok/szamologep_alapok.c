#include <stdio.h>

int main()
{
    //osszeadas
    printf("\nElso szam: %d, masodik szam: %d, osszeg: %d", 10, 11, 10+11);

    //kivonas
    printf("\nAz elso: %.1f, a masodik %d, kulonbseg %.1f", 10.9, 10, 10.9-10);

    //szorzas
    printf("\nAz elso: %d, a masodik %d, szorzatuk %d", 10, 10, 10*10);

    //osztas
    printf("\nAz elso: %d, a masodik %d, hanyadosuk %d", 10, 4, 10/4);


    //szazalek szamitas
    printf("\nAz elso: %d, a masodik %d, szazalek %d", 3, 10, (3+10)*50);



    return 0;
}